package com.example.losts.Posts;

public class Post {


   public String Date, Description, Category, UserID,Location, Status, Image;

    public Post() {
    }

    public Post(String date, String description, String category, String userID, String status, String location, String image) {
        Date = date;
        Description = description;
        Category = category;
        UserID = userID;
        Status = status;
        Location = location;
        Image = image;
        Status = status;
    }
}
